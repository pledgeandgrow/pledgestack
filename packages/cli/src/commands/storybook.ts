/**
 * pledge storybook — Zero-config Storybook integration for PledgeStack.
 *
 * Goal #223: Auto-configures Storybook for PledgeStack projects:
 * - Generates .storybook/ directory with PledgeStack-aware config
 * - Detects app/ components and generates .stories.tsx files
 * - Configures Storybook to use PledgeStack's React 19 + Tailwind setup
 * - Supports both server and client components in stories
 */

import { readFile, writeFile, mkdir, readdir, access } from 'node:fs/promises';
import { existsSync } from 'node:fs';
import { join, relative } from 'node:path';
import type { PledgeConfig } from 'pledgestack-shared';

interface StorybookOptions {
  /** Force overwrite existing .storybook/ config */
  force?: boolean;
  /** Generate stories for all components (default: only components without stories) */
  all?: boolean;
}

/**
 * Generates the .storybook/main.ts config.
 */
function generateMainConfig(config: PledgeConfig): string {
  return `import type { StorybookConfig } from './types';

const config: StorybookConfig = {
  stories: [
    '../${config.appDir}/**/*.stories.@(ts|tsx|mdx)',
    '../${config.appDir}/**/*.@(ts|tsx)',
  ],
  addons: [
    '@storybook/addon-essentials',
    '@storybook/addon-interactions',
  ],
  framework: {
    name: '@storybook/react-vite',
    options: {},
  },
  docs: {
    autodocs: 'tag',
  },
  typescript: {
    check: false,
    reactDocgen: 'react-docgen-typescript',
    reactDocgenTypescriptOptions: {
      shouldExtractLiteralValuesFromEnum: true,
      propFilter: (prop: { parent?: { fileName?: string } }) =>
        prop.parent ? !/node_modules/.test(prop.parent.fileName ?? '') : true,
    },
  },
  staticDirs: ['../${config.publicDir}'],
};

export default config;
`;
}

